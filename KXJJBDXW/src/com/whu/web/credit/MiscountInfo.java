package com.whu.web.credit;

public class MiscountInfo {
	private String id;
	private String miscountId;
	private String title;
	private String individual;
	private String institute;
	private String reportId;
	private String mistype;
	private String punish;
	private String time;
	private String detail;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMiscountId() {
		return miscountId;
	}
	public void setMiscountId(String miscountId) {
		this.miscountId = miscountId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getIndividual() {
		return individual;
	}
	public void setIndividual(String individual) {
		this.individual = individual;
	}
	public String getInstitute() {
		return institute;
	}
	public void setInstitute(String institute) {
		this.institute = institute;
	}
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getMistype() {
		return mistype;
	}
	public void setMistype(String mistype) {
		this.mistype = mistype;
	}
	public String getPunish() {
		return punish;
	}
	public void setPunish(String punish) {
		this.punish = punish;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
}
